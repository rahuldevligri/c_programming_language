#include<stdio.h>
int main(){
int x=2,y=3;
printf("The value of 8*y-3*x is %d\n",8*y-3*x); // 8x3-3x2=24-6=18
printf("The value of 3*x-8*y is %d\n",3*x-8*y); // 3x2-8x3=6-24=18

printf("The value of 8*y/3*x is %d\n",8*y/3*x); // 8x3/3x2=24/3x2=8x2=16
printf("The value of 3*x/8*y is %d\n",3*x/8*y); // 3x2/8x3=6/24=0

return 0;
}

